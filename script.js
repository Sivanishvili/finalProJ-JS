function main() {
  const wrapper = document.getElementById("cards");

  for (let cardObj of CARDS) {
    // create card
    const card = document.createElement("div");
    card.classList.add("card");
    wrapper.appendChild(card);

    //img
    const img = document.createElement("img");
    img.src = cardObj.imgUrl;
    img.classList.add("img");
    card.appendChild(img);

    // create title 
    const title = document.createElement("div");
    title.classList.add("title");
    title.innerHTML = cardObj.title;
    card.appendChild(title);
    
    // description
    const description = document.createElement("div");
    description.classList.add("description");
    description.innerHTML = cardObj.description;
    card.appendChild(description);

    //description2
    const description2 = document.createElement("div");
    description2.classList.add("description2");
    description2.innerHTML = cardObj.description2;
    card.appendChild(description2);


    //svg
    const img2 = document.createElement("img");
    img2.src = cardObj.img2Url;
    img2.classList.add("img2");
    card.appendChild(img2);


}

}

main();
